import { generateKeyPairSync } from "node:crypto";
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const webRoot = path.resolve(__dirname, "..");

function escapeForEnv(pem) {
  return pem.replace(/\r?\n/g, "\\n");
}

function toEnvLine(name, pem) {
  return `${name}="${escapeForEnv(pem)}"`;
}

const { publicKey, privateKey } = generateKeyPairSync("rsa", {
  modulusLength: 2048,
  publicKeyEncoding: { type: "spki", format: "pem" },
  privateKeyEncoding: { type: "pkcs8", format: "pem" },
});

const publicPem = publicKey.trim();
const privatePem = privateKey.trim();

const lines = [
  "# Generated by: bun run --filter web keys:analyze",
  "# Append these lines to apps/web/.env, then restart dev server and re-login in the extension.",
  "",
  toEnvLine("ANALYZE_PAYLOAD_PUBLIC_KEY", publicPem),
  toEnvLine("ANALYZE_PAYLOAD_PRIVATE_KEY", privatePem),
  toEnvLine("DEEP_SCAN_PUBLIC_KEY", publicPem),
  toEnvLine("DEEP_SCAN_PRIVATE_KEY", privatePem),
  "",
  "# Local Deep Scan inference (decrypt + analyze on this app)",
  "DEEP_SCAN_INFERENCE_URL=http://localhost:3001/api/internal/deep-scan-inference",
  "",
];

const outArg = process.argv.find((arg) => arg.startsWith("--out="));
const outRel = outArg ? outArg.slice("--out=".length) : ".env.analyze.keys";
const outPath = path.isAbsolute(outRel)
  ? outRel
  : path.join(webRoot, outRel.replace(/^apps[/\\]web[/\\]/, ""));

await mkdir(path.dirname(outPath), { recursive: true });
await writeFile(outPath, `${lines.join("\n")}\n`, "utf8");

console.log(`Wrote key env snippet to:\n  ${outPath}`);
console.log("\nNext steps:");
console.log("  1. Copy the lines into apps/web/.env");
console.log("  2. Restart: bun run dev:web");
console.log("  3. Extension: sign out/in via http://localhost:3001/ext-auth");
console.log("  4. Gmail: open a medium/high risk email and click Deep Scan");
